DO $$
BEGIN
  IF NOT EXISTS (
    SELECT 1 FROM pg_policies
    WHERE schemaname = 'public'
      AND tablename = 'rfqs'
      AND policyname = 'Public users can read public open RFQs'
  ) THEN
    CREATE POLICY "Public users can read public open RFQs"
      ON public.rfqs
      FOR SELECT
      USING (is_public = TRUE AND status = 'open');
  END IF;
END $$;

DO $$
BEGIN
  IF NOT EXISTS (
    SELECT 1 FROM pg_policies
    WHERE schemaname = 'public'
      AND tablename = 'rfqs'
      AND policyname = 'Authenticated users can read RFQs'
  ) THEN
    CREATE POLICY "Authenticated users can read RFQs"
      ON public.rfqs
      FOR SELECT
      TO authenticated
      USING (TRUE);
  END IF;
END $$;

DO $$
BEGIN
  IF NOT EXISTS (
    SELECT 1 FROM pg_policies
    WHERE schemaname = 'public'
      AND tablename = 'rfqs'
      AND policyname = 'Buyers can manage their own RFQs'
  ) THEN
    CREATE POLICY "Buyers can manage their own RFQs"
      ON public.rfqs
      FOR ALL
      TO authenticated
      USING (created_by = auth.uid() OR buyer_user_id = auth.uid())
      WITH CHECK (created_by = auth.uid() OR buyer_user_id = auth.uid());
  END IF;
END $$;
