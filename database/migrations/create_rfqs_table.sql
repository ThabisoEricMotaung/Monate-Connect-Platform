-- Creates or stabilizes the RFQ marketplace schema used by Monate Connect.
-- The live application already uses numeric RFQ IDs for quote, award, and route
-- references, so this migration keeps that contract and adds the marketplace
-- fields required by the seeded procurement data.

CREATE EXTENSION IF NOT EXISTS pgcrypto;

CREATE TABLE IF NOT EXISTS public.rfqs (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  title TEXT NOT NULL,
  description TEXT,
  scope TEXT,
  buyer_name TEXT,
  buyer_org TEXT,
  buyer_user_id UUID REFERENCES auth.users(id) ON DELETE SET NULL,
  industry TEXT,
  provinces TEXT[] DEFAULT '{}',
  bbbee_requirement TEXT,
  estimated_value_min NUMERIC,
  estimated_value_max NUMERIC,
  closing_date TIMESTAMPTZ,
  published_date TIMESTAMPTZ DEFAULT NOW(),
  status TEXT DEFAULT 'open',
  require_csd BOOLEAN DEFAULT TRUE,
  require_tax_clearance BOOLEAN DEFAULT TRUE,
  require_vat BOOLEAN DEFAULT FALSE,
  is_public BOOLEAN DEFAULT TRUE,
  quote_count INTEGER DEFAULT 0,
  category TEXT,
  province TEXT,
  region TEXT,
  budget TEXT,
  deadline DATE,
  created_by UUID REFERENCES auth.users(id) ON DELETE SET NULL,
  is_demo BOOLEAN DEFAULT FALSE,
  created_at TIMESTAMPTZ DEFAULT NOW(),
  updated_at TIMESTAMPTZ DEFAULT NOW()
);

