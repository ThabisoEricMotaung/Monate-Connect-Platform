UPDATE public.rfqs
SET
  buyer_org = COALESCE(buyer_org, buyer_name),
  industry = COALESCE(industry, category),
  provinces = CASE
    WHEN provinces IS NULL OR array_length(provinces, 1) IS NULL
      THEN ARRAY_REMOVE(ARRAY[province, region], NULL)
    ELSE provinces
  END,
  closing_date = COALESCE(closing_date, deadline::timestamptz),
  published_date = COALESCE(published_date, created_at),
  is_public = COALESCE(is_public, TRUE),
  quote_count = COALESCE(quote_count, 0);

CREATE UNIQUE INDEX IF NOT EXISTS rfqs_seed_title_buyer_org_key
  ON public.rfqs (title, buyer_org);

